#include "libft.h"

int	atoi(const char *str)
{
	int	num;
	int	neg;

	neg = 0;
	while (*str == ' ' || (*str >= 9 && *str <= 13))
		str++;
	if (*str == '-' || *str == '+')
	{
		if (*str == '-')
			neg = neg + 1;
	}
	while (*str >= '0' && *str <= '9')
	{
		num = *str - '0';
	}
	if (neg % 2 != 0)
	{
		num = num * (-1);
	}
	return (num);
}
